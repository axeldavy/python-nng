async def main(transport: str, event_interval: float) -> None:
    """Run the subscription server.

    Args:
        transport: NNG transport for PAIR sockets.
        event_interval: Seconds between emitted events.
    """
    server_signing_key = load_signing_key(_SERVER_KEY_PATH)
    server_vk_hex = derive_verify_key(server_signing_key).encode().hex()
    _LOG.info("Server identity  verify_key = %s", server_vk_hex)
    _LOG.info("Listening for subscriptions on %s  (transport=%s)", DEFAULT_REP_URL, transport)

    server = SubscriptionReqServer(
        server_signing_key=server_signing_key,
        authorized_vks=AUTHORIZED_CLIENT_KEYS,
        transport=transport
    )
    async with asyncio.TaskGroup() as tg:
        server.serve(tg, DEFAULT_REP_URL)
    _LOG.info("Server shutting down")


def _parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--transport",
        choices=["tcp", "ipc", "inproc"],
        default="tcp",
        help="Transport for the PAIR feed socket (default: tcp)",
    )
    parser.add_argument(
        "--interval",
        type=float,
        default=DEFAULT_EVENT_INTERVAL_S,
        metavar="SECONDS",
        help="Event emission interval in seconds (default: %(default)s)",
    )
    return parser.parse_args()


if __name__ == "__main__":
    args = _parse_args()
    try:
        asyncio.run(main(args.transport, args.interval))
    except KeyboardInterrupt:
        print("\nServer interrupted — shutting down.")
