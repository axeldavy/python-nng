#!/usr/bin/env python3
"""Subscription server — run this before starting any client.

Architecture
------------
A single :class:`nng.RepSocket` listens for subscription requests on
``REP_URL``.  When a request arrives from an authorized client it:

1. Validates the client's Ed25519 signature.
2. Delegates to :class:`~common.SecurePairServer`, which generates ephemeral
   keys, binds the PAIR listener, sends the signed response, and completes
   the mutual-auth handshake.
3. Spawns a background task that streams JSON events over the authenticated,
   encrypted PAIR channel.

Multiple clients may be subscribed concurrently.  Each gets its own PAIR
socket and its own asyncio task.  The REP loop remains responsive throughout.

Usage
-----
    python server.py [--transport tcp|ipc|inproc] [--interval SECONDS]
"""

