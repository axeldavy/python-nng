async def generate_events(
    topic: str, *, interval: float
) -> AsyncGenerator[dict[str, Any], None]:
    """Yield one JSON-serialisable event every *interval* seconds.

    Args:
        topic: The topic string requested by the client.
        interval: Emission cadence in seconds.

    Yields:
        A dict with ``topic``, ``seq``, ``ts``, and ``value`` fields.
    """
    seq = 0
    while True:
        await asyncio.sleep(interval)
        seq += 1
        yield {
            "topic": topic,
            "seq": seq,
            "ts": datetime.datetime.now(tz=datetime.timezone.utc).isoformat(),
            "value": round(math.sin(seq * 0.3), 6),
        }



# ---------------------------------------------------------------------------
# Per-client PAIR session
# ---------------------------------------------------------------------------

class EphemeralPairServer(SecurePairServer):
    """Server for the REP/REQ subscription protocol with post-handshake token verification."""

    __slots__ = {
        "_connection_token": "32-byte opaque token expected from client after handshake.",
    }

    _connection_token: bytes | None
    _url: str

    def __init__(self, *args: Any, **kwargs: Any) -> None:
        super().__init__(*args, **kwargs)
        self._connection_token = None

    @staticmethod
    def find_available_addr_and_create(
        authorized_vks: frozenset[nacl.signing.VerifyKey],
        server_signing_key: nacl.signing.SigningKey,
        transport: str,
        connection_token: bytes,
    ) -> EphemeralPairServer:
        """Find an available address and create a PAIR socket bound to it.

        Args:
            authorized_vks: The set of authorized client verify keys.
            server_signing_key: The server's signing key.
            transport: NNG transport to use (e.g. ``tcp``, ``ipc``, ``inproc``).
            connection_token: The opaque token expected from the client after handshake.
        """
        pair_addr = pick_pair_addr(transport)
        server = EphemeralPairServer.create(
            authorized_vks,
            server_signing_key,
            pair_addr
        )
        server._connection_token = connection_token
        return server

    @override
    def _connect(self) -> None:
        """PAIR handshake then token verification (sync)."""
        super()._connect()
        token = self.recv()
        if token != self._connection_token:
            self._pair.close()
            raise PermissionError("Connection token mismatch — client did not go through subscription")

    @override
    async def _aconnect(self) -> None:
        """PAIR handshake then token verification (async)."""
        await super()._aconnect()
        token = await self.arecv()
        if token != self._connection_token:
            self._pair.close()
            raise PermissionError("Connection token mismatch — client did not go through subscription")


async def serve_client(
    srv: EphemeralPairServer,
    feed: AsyncGenerator[dict[str, Any], None],
) -> None:
    """Complete the handshake and stream events to one client.

    This coroutine is launched as a fire-and-forget task
    by the subscription manager once the REP handshake is done and the PAIR
    server is ready.  It performs the PAIR handshake, then enters a loop to
    stream events until the client disconnects or an error occurs.

    Args:
        srv: The PAIR server instance, already configured with the client's verify key
            and the connection token.
        feed: An async generator yielding JSON-serialisable events to send to the client.
    """
    client_id = os.urandom(6).hex()  # Dummy client ID for logging 
    try:
        async with aclosing(feed) as feed:
            async with srv as conn:
                _LOG.info("Client %s handshake complete — beginning streaming", client_id)
                async for event in feed:
                    binary = json.dumps(event, separators=(",", ":")).encode("utf-8")
                    await conn.asend(binary)
                    _LOG.debug("Client %s → seq=%s", client_id, event["seq"])
    except TimeoutError:
        _LOG.warning("Client %s handshake timed out — dropping connection", client_id)
    except (nacl.exceptions.BadSignatureError, ValueError) as exc:
        _LOG.warning("Client %s handshake FAILED: %s", client_id, exc)
    except nng.NngClosed:
        _LOG.info("Client %s disconnected", client_id)
    except asyncio.CancelledError:
        _LOG.info("Client %s task cancelled", client_id)
        raise

