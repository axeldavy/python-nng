class SubscriptionReqServer(SecureRepServer):
    """Secure REP server that handles subscription requests and spawns PAIR servers.

    This class is responsible for the REP-side handshake and response to
    subscription requests.  The actual PAIR handshake and event streaming is
    handled by :class:`~common.SecurePairServer` in the per-client tasks.
    """

    __slots__ = {
        "_transport": "NNG transport for PAIR sockets (e.g. tcp, ipc, inproc).",
    }

    _transport: str

    def __init__(
        self,
        server_signing_key: nacl.signing.SigningKey,
        authorized_vks: frozenset[nacl.signing.VerifyKey],
        transport: str
    ) -> None:
        super().__init__(server_signing_key, authorized_vks)
        self._transport = transport

    @override
    async def conversation(self, conv) -> None:
        assert self._tg is not None
        _LOG.info(f"Starting new conversation with {conv.pipe.get_peer_addr()}")

        while True:
            # Get subscription topic from the client
            raw = await conv.arecv()

            # Interprete the request
            try:
                req = SubscribeRequest.from_json(raw)
            except (ValueError, KeyError) as exc:
                _LOG.warning("Malformed subscription request: %s", exc)
                await conv.asend(ErrorResponse(reason=str(exc)).to_json())
                raise # End conversation

            # prepare random token for client to send back after PAIR handshake
            token = os.urandom(32)

            # Spawn the PAIR server and send the response (url + random token)
            pair = EphemeralPairServer.find_available_addr_and_create(
                AUTHORIZED_CLIENT_KEYS,
                self._server_signing_key,
                self._transport,
                token
            )
            # TODO
            response = SubscribeResponse.create(
                pair.pair_addr, token
            )

            # Reply to client (the server already listens for connections)
            await conv.asend(response.to_json())

            # Create the pair feed
            feed = generate_events(req.topic, interval=DEFAULT_EVENT_INTERVAL_S)

            # Start the PAIR conversation in a new task
            self._tg.create_task(
                serve_client(pair, feed),
                name=f"client-{req.topic}-{os.urandom(3).hex()}"
            )
        # Return automatically when client disconnects (arecv will raise ConnectionResetError))
# ---------------------------------------------------------------------------
# Entry point
